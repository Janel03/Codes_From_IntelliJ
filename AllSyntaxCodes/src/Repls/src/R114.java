public class R114 {
    public static void main(String[] args) {
        addition(15,15);
        multiplication(15,2);
        subtraction(40,2);
    }


    private static void addition(int a, int b) {
        System.out.println(a+b);
    }
    private static void multiplication(int c, int d) {
        System.out.println(c*d);
    }
    private static void subtraction(int e, int f) {
        System.out.println(e/f);
    }

}
